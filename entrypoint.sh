#!/bin/bash
set -e

echo "Running database migrations..."
alembic upgrade head || echo "Migration failed, attempting to continue..."

echo "Starting ReminderBot Pro server..."
exec gunicorn app.main:app \
    --worker-class uvicorn.workers.UvicornWorker \
    --workers 4 \
    --bind 0.0.0.0:8000 \
    --timeout 120 \
    --keep-alive 5 \
    --max-requests 1000 \
    --max-requests-jitter 50 \
    --access-logfile - \
    --error-logfile - \
    --log-level info
